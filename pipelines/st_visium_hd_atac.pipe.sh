function run_spaceranger(){

    slide_number=$(basename -s ".vlf" $slide)
    cd $outdir
    if [[ ! -d $outdir/trim/R2/ ]];then
        mkdir -p $outdir/trim/R2/
    fi

    $cutadapt -j $thread_trim \
        -G "^GGAGTGTGAAGACAGAGATGTGTATAAGAGACAG" \
        -e 0.1 \
        -o $outdir/trim/R2/${sampleid}_IVT.R1.fastq.gz \
        -p $outdir/trim/R2/${sampleid}_IVT.R2.fastq.gz \
        $read1 $read2

    mv $outdir/trim/R2/${sampleid}_IVT.R1.fastq.gz $outdir/trim/R2/${sampleid}_S1_L002_R1_001.fastq.gz
    mv $outdir/trim/R2/${sampleid}_IVT.R2.fastq.gz $outdir/trim/R2/${sampleid}_S1_L002_R2_001.fastq.gz
    fastq_dir=$(dirname $outdir/trim/R2/${sampleid}_S1_L002_R2_001.fastq.gz)

    $spaceranger count --id=$sampleid \
        --transcriptome=$visium_ref \
        --create-bam=true \
        --fastqs=$fastq_dir \
        --sample=$sampleid \
        --cytaimage=$tif \
        --slide=$slide_number \
        --slidefile=$slide \
        --area=$area \
        --localcores=$SLURM_CPUS_PER_TASK \
        --project=$sampleid \
        --localmem=150 \
        --custom-bin-size 50 \
        --loupe-alignment ../data/${sampleid}.json
    cd $outdir/..

}

