function stat(){

    if [[ ! -d $outdir/stat ]];then 
        mkdir -p $outdir/stat 
    fi 
     bam="$outdir/bulk/${sampleid}.clean.bam"
    peaks="$outdir/bulk/${sampleid}_peaks.narrowPeak"

    # check file
    if [ ! -s $bam ]; then
        echo $bam " not exist"
        exit
    fi

    if [ ! -s $peaks ]; then
        echo $peaks "peaks not exist"
        exit
    fi

    echo "→ Computing FRiP for $sampleid …"

    # 1) total reads
    total_reads=$(samtools view -c -@ 12 "$bam")

    # 2) reads in peaks
    reads_in_peaks=$( \
        $bedtools sort -i "$peaks" \
        | $bedtools merge -i stdin \
        | $bedtools intersect -u -nonamecheck \
            -a "$bam" \
            -b stdin -ubam \
        | samtools view -c \
    )

    # 3) 计算 FRiP（保留四位小数）
    frip=$(awk 'BEGIN{printf "%.4f", '$reads_in_peaks'/'$total_reads'}')

    total_raw_reads=$(grep Total $outdir/barcode/${sampleid}.barcode_stat.txt | awk '{print $2}')
    valid_barcode_reads=$(grep Valid $outdir/barcode/${sampleid}.barcode_stat.txt | awk '{print $2}')

    valid_barcode_percent=$(echo "scale=2; $valid_barcode_reads / $total_raw_reads * 100" | bc | xargs)
    reads_passing_filter=$(grep 'output":' $outdir/trim/${sampleid}.trimming_report.txt | awk '{print $2}' | head -n 1 | sed 's/,//g')
    reads_passing_filter_rate=$(echo "scale=2; $reads_passing_filter / $valid_barcode_reads * 100" | bc | xargs)
    
    unique_align_reads=$($samtools view -q 20 -c -@ 12 $outdir/align/${sampleid}.sorted.bam | xargs)

    align_rate=$(echo "scale=2; $unique_align_reads / $reads_passing_filter * 100" | bc | xargs)

    if [[ ! -s ${outdir}/stat/stat.txt ]]; then
        echo -e "SampleID\tSpecies\tTotal_reads\tBarcode_valid_percent\tReads_high_qualtity_percent\tUnique_aligned_reads\tUnique_align_rate\tFRiP" > ${outdir}/stat/stat.txt
    fi

    echo -e "$sampleid\t${species}\t" \
        "${total_raw_reads}\t" \
        "${valid_barcode_percent}%\t" \
        "${reads_passing_filter_rate}%\t" \
        "${unique_align_reads}\t" \
        "${align_rate}\t" \
        "${frip}" \
        >>${outdir}/stat/stat.txt

}


