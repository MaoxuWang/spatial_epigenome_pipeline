function stat(){

    if [[ ! -d $outdir/stat ]];then 
        mkdir -p $outdir/stat 
    fi 
    
    total_reads=$($samtools view -@ $SLURM_CPUS_PER_TASK -c results/${sampleid}/${sampleid}.shifted.bam )

    # reads in peaks
    reads_in_peaks=$($bedtools sort -i results/bulk/${sampleid}_peaks.narrowPeak | $bedtools merge -i stdin | $bedtools intersect -u -nonamecheck \
        -a results/${sampleid}/${sampleid}.shifted.bam -b stdin -ubam | $samtools view -@ $SLURM_CPUS_PER_TASK -c)
    # FRiP score
    FRiP=$(awk "BEGIN {print "${reads_in_peaks}"/"${total_reads}"}")

    echo $sampleid $FRiP | tr ' ' '\t' > $outdir/stat/${sampleid}_FRiP.txt

}


