function bulk(){
    ### calculate as bulk samples
    if [[ ! -d $outdir/bulk ]];then 
        mkdir -p $outdir/bulk
    fi 
    
    ## step 1. dedup by UMI_tools
    $umi_tools dedup \
        --umi-tag="CB" \
        --cell-tag="CB" \
        --extract-umi-method="tag" \
        -I $outdir/align/${sampleid}.super_spot.bam \
        --output-stats=$outdir/bulk/${sampleid}.deduplicated \
        -S $outdir/bulk/${sampleid}.rmdup.bam \
        --method unique 


    ## step 2 shift alignments
    $samtools view -bh -q 30 \
        $outdir/bulk/${sampleid}.rmdup.bam \
    | $samtools sort -@ $thread_sort -m "12G" \
    > $outdir/bulk/${sampleid}.dedup.bam

    $samtools index -@ $SLURM_CPUS_PER_TASK $outdir/bulk/${sampleid}.dedup.bam
    rm $outdir/bulk/${sampleid}.rmdup.bam

    ## filter the chrM reads
    $samtools idxstats -@ $SLURM_CPUS_PER_TASK $outdir/bulk/${sampleid}.dedup.bam \
        | cut -f 1 | grep -v MT \
        | xargs $samtools view -b $outdir/bulk/${sampleid}.dedup.bam > $outdir/bulk/${sampleid}.clean.bam
    
    # $Rscript $src/shiftAlignment.R \
    #     $outdir/bulk/${sampleid}.clean.bam \
    #     $outdir/bulk/${sampleid}.shifted.bam
    $samtools index -@ $SLURM_CPUS_PER_TASK $outdir/bulk/${sampleid}.clean.bam

    effect_genome_size=2652783500
    
    $bamCoverage --numberOfProcessors $SLURM_CPUS_PER_TASK \
        --binSize 10 \
        --normalizeUsing RPGC \
        --effectiveGenomeSize $effective_genome_size \
        --bam $outdir/bulk/${sampleid}.clean.bam \
        -o $outdir/bulk/${sampleid}.clean.bw



    # ## call peak
    # $macs2 callpeak \
    #     -t $outdir/bulk/${sampleid}.shifted.bam \
