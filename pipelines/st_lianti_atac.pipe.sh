function select_Tn5_reads(){
    if [[ ! -d $outdir/trim ]];then 
        mkdir -p $outdir/trim
    fi 
    
    ## Select by R2
    $cutadapt -j $thread_trim \
        -G "^AAGCAGTGGTATCAACGCAGAGTACATAGATGTGTATAAGAGACAG" \
        -e 3 \
        -o $outdir/trim/${sampleid}_IVT_R1.fastq.gz \
        -p $outdir/trim/${sampleid}_IVT_R2.fastq.gz \
        --untrimmed-output $outdir/trim/${sampleid}_RNA_R1.fastq.gz \
        --untrimmed-paired-output $outdir/trim/${sampleid}_RNA_R2.fastq.gz \
        $read1 $read2
    
}  

function trim_ME(){
    ## When compared ME reads vs. non-ME reads
    if [[ ! -d $outdir/trim ]];then 
        mkdir -p $outdir/trim
    fi 
    if [[ ! -s $outdir/trim/${sampleid}_ME_R1.fastq.gz ]];then 
        $cutadapt \
            -j $thread_trim \
            -G "^NAGATGTGTATAAGAGACAG" \
            -G "^NNAGATGTGTATAAGAGACAG" \
            -G "^NNNAGATGTGTATAAGAGACAG" \
            -G "^NNNNAGATGTGTATAAGAGACAG" \
            --no-indels -e 2 \
            -o $outdir/trim/${sampleid}_ME_R1.fastq.gz \
            -p $outdir/trim/${sampleid}_ME_R2.fastq.gz \
            --untrimmed-output $outdir/trim/${sampleid}_woME_R1.fastq.gz \
            --untrimmed-paired-output $outdir/trim/${sampleid}_woME_R2.fastq.gz \
            $read1 $read2
    else
        echo "ME sequence is trimmed already."
    fi 
}


