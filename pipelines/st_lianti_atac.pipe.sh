function barcode_calling(){
    if [[ ! -d $outdir/barcode ]];then 
        mkdir -p $outdir/barcode
    fi 
    # export PERL5LIB="/usr/lib64/perl5/vendor_perl"
    read1=$outdir/trim/${sampleid}_IVT_R1.fastq.gz
    read2=$outdir/trim/${sampleid}_IVT_R2.fastq.gz
      
    if [[ ! -s $outdir/barcode/$sampleid.umi_cor.info ]];then
        $BST_src/fastq2BcUmi -i1 $read1 \
            -i2 $read2 \
            -d $outdir/barcode \
            -p $sampleid \
            -n $SLURM_CPUS_PER_TASK
    else
        echo "fastq2BCUmi is finished!"
    fi 

    if [[ $n_line -eq "-1" ]];then 
        n_line=$(head -n 1 $outdir/barcode/$sampleid.stat | awk '{print $2 * 4}' | xargs)
    fi
    echo "$n_line is processing..."

    $spatial_IVT tag2fq \
        --fastq $read2 \
        --bc_read_map $outdir/barcode/$sampleid.umi \
        -o $outdir/barcode \
        --n_lines $n_line \
        --sampleName $sampleid \
        --threads $SLURM_CPUS_PER_TASK \
        --out_unmapped \
        --umi_cor $outdir/barcode/$sampleid.umi_cor.info \
        --verbose
}


