function pseudo_bulk(){
    if [[ ! -d $outdir/trim ]];then 
        mkdir -p $outdir/trim
    fi 

    $cutadapt -j $thread_trim \
        -q 20 \
        -a "A{9};max_error_rate=0.2;min_overlap=3" \
        --trim-n \
        --no-indels \
        --minimum-length $read_len \
        --json $outdir/trim/$sampleid.trimming_report.txt \
        -e 2 \
        -o $outdir/trim/${sampleid}_clean_barcoded.R1.fastq.gz \
        $read1

    if [[ ! -d $outdir/align ]];then 
        mkdir -p $outdir/align
    fi 

    $bowtie2 -x $bowti2_idx \
        -U $outdir/trim/${sampleid}_clean_barcoded.R1.fastq.gz \
        --no-unal \
        -p $SLURM_CPUS_PER_TASK \
        | $samtools sort -@ $thread_sort -m "12G" \
        | $samtools view -q 30 -b \
        > $outdir/align/${sampleid}.sorted.bam
    
    if [[ ! -d $outdir/bulk ]];then 
        mkdir -p $outdir/bulk
    fi 
    $picard MarkDuplicates \
        -I $outdir/align/${sampleid}.sorted.bam \
        -O $outdir/bulk/${sampleid}.rmdup.bam \
        -M $outdir/bulk/${sampleid}.dedup.txt \
        -REMOVE_DUPLICATES true \
        -READ_NAME_REGEX null \
        --TMP_DIR ${outdir}/bulk
    $samtools index -@ $SLURM_CPUS_PER_TASK $outdir/bulk/${sampleid}.rmdup.bam

    # Remove chrM alignments before bulk coverage and peak calling.
    $samtools idxstats -@ $SLURM_CPUS_PER_TASK $outdir/bulk/${sampleid}.rmdup.bam \
        | cut -f 1 | grep -v MT \
        | xargs $samtools view -b $outdir/bulk/${sampleid}.rmdup.bam > $outdir/bulk/${sampleid}.temp.bam
    


    # Keep high-confidence alignments for bulk coverage and peak calling.
    $samtools view -bh -q 30 -@ $SLURM_CPUS_PER_TASK \
        -o $outdir/bulk/${sampleid}.clean.bam \
        $outdir/bulk/${sampleid}.temp.bam

    $samtools index -@ $SLURM_CPUS_PER_TASK $outdir/bulk/${sampleid}.clean.bam

    
    $bamCoverage --numberOfProcessors $SLURM_CPUS_PER_TASK \
        --binSize 10 \
        --normalizeUsing RPGC \
        --effectiveGenomeSize $effective_genome_size \
        --bam $outdir/bulk/${sampleid}.clean.bam \
        -o $outdir/bulk/${sampleid}.clean.bw

    $macs2 callpeak \
        -f BAM \
        -g ${MACS2_GENOME[$species]} \
        --nomodel --shift -100 --extsize 200 \
        --keep-dup all --qvalue 0.01 \
        -t $outdir/bulk/${sampleid}.clean.bam \
        -n $sampleid \
        --outdir $outdir/bulk

}


