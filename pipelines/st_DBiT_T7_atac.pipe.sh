function stat_abortive(){
    
    if [[ ! -d $outdir/stat_abortive ]];then 
        mkdir -p $outdir/stat_abortive
    fi 
    thread_trim=8

    head -n 400000 <(zcat $read1) | pigz > $outdir/stat_abortive/$sampleid.100K.R1.fastq.gz

    echo "Downsampling to 100K reads..."

    $cutadapt -j $thread_trim \
        -a "A{9};max_error_rate=0.2;min_overlap=3" \
        --trim-n \
        --no-indels \
        --json $outdir/stat_abortive/$sampleid.trimming_report.txt \
        -e 2 \
        -o $outdir/stat_abortive/${sampleid}_polyA_trimmed.R1.fastq.gz \
        $outdir/stat_abortive/$sampleid.100K.R1.fastq.gz

    $seqkit fx2tab $outdir/stat_abortive/${sampleid}_polyA_trimmed.R1.fastq.gz \
        -l -i -Q -n > $outdir/stat_abortive/$sampleid.len.stat.txt
    awk '{print $2}' $outdir/stat_abortive/$sampleid.len.stat.txt > $outdir/stat_abortive/$sampleid.len.stat.tmp
    mv $outdir/stat_abortive/$sampleid.len.stat.tmp $outdir/stat_abortive/$sampleid.len.stat.txt

    $Rscript $src/plot_abortive_distribution.R $outdir/stat_abortive/$sampleid.len.stat.txt $outdir/stat_abortive/$sampleid

}


function barcode_calling(){
    if [[ ! -d $outdir/barcode ]];then 
        mkdir -p $outdir/barcode
    fi 

    if [ ! -s $read1 ]; then
        echo $read1 "Read1 not exist"
        exit
    fi

    if [ ! -s $read2 ]; then
        echo $read2 "Read2 not exist"
        exit
    fi


    if [[ $n_line -eq "-1" ]];then 
        if [[ -s $outdir/barcode/${sampleid}.total_readsN.txt ]];then
            n_line=$(head -n 1 $outdir/barcode/${sampleid}.total_readsN.txt | awk '{print $1 * 4}' | xargs) 
            echo "$n_line is processing..."
        else
            echo "All lines is processing..."
        fi
    fi

    if [[ $paired == "TRUE" ]];then
        $python_cv2 $extract_barcodes \
            --read1 $read1 \
            --read2 $read2 \
            -o $outdir/barcode \
            --n_lines $n_line \
            --sampleName $sampleid \
            --paired \
            --threads $SLURM_CPUS_PER_TASK
    else
        $python_cv2 $extract_barcodes \
            --read1 $read1 \
            --read2 $read2 \
            -o $outdir/barcode \
            --n_lines $n_line \
            --sampleName $sampleid \
            --threads $SLURM_CPUS_PER_TASK \
            --umi_array "(98, 108)" \
            --cb_array "(22, 30)" "(60, 68)"
    fi 

}

function trim_fq(){
    if [[ $SLURM_CPUS_PER_TASK -ge 12 ]]; then
        thread_trim=12
    else
        thread_trim=$SLURM_CPUS_PER_TASK
    fi 

    if [[ ! -d $outdir/trim ]];then 
        mkdir -p $outdir/trim
    fi 

    if [[ ! -s $outdir/trim/${sampleid}_clean.R2.fastq.gz ]];then 

        if [[ $paired == "TRUE" ]];then
            $cutadapt -j $thread_trim \
                --poly-a \
                -a "A{9};max_error_rate=0.2;min_overlap=3" \
                -a "CTGTCTCTTATACACATCT" \
                -A "CTGTCTCTTATACACATCT" \
                --trim-n \
                --minimum-length $read_len \
                --json $outdir/trim/$sampleid.trimming_report.txt \
                -o $outdir/trim/${sampleid}_clean_barcoded.R1.fastq.gz \
                -p $outdir/trim/${sampleid}_clean_barcoded.R2.fastq.gz \
                $outdir/barcode/${sampleid}.R1.barcoded.fastq.gz \
                $outdir/barcode/${sampleid}.R2.barcoded.fastq.gz
        else
            $cutadapt -j $thread_trim \
                -q 20 \
                -a "A{9};max_error_rate=0.2;min_overlap=3" \
                --trim-n \
                --no-indels \
                --minimum-length $read_len \
                --json $outdir/trim/$sampleid.trimming_report.txt \
                -e 2 \
                -o $outdir/trim/${sampleid}_clean_barcoded.R1.fastq.gz \
                $outdir/barcode/${sampleid}.R1.barcoded.fastq.gz
        fi 
    else
        echo "sequence is trimmed already."
    fi 
}

function trim_Nextera(){
    if [[ $SLURM_CPUS_PER_TASK -ge 12 ]]; then
        thread_trim=12
    else
        thread_trim=$SLURM_CPUS_PER_TASK
    fi 

    if [[ ! -d $outdir/trim ]];then 
        mkdir -p $outdir/trim
    fi 

    if [[ ! -s $outdir/trim/${sampleid}_clean.R2.fastq.gz ]];then 
        $trim_galore \
            --paired \
            --nextera \
            --stringency 5 \
            --cores $thread_trim \
            -o $outdir/trim \
            $outdir/barcode/${sampleid}.R1.barcoded.fastq.gz \
            $outdir/barcode/${sampleid}.R2.barcoded.fastq.gz
    else
        echo "sequence is trimmed already."
    fi 

    ## CB:UMI:readname
    $bowtie2 -x $bowti2_idx \
        --no-unal \
        -X 2000 \
        -1 $outdir/trim/${sampleid}.R1_val_1.fq.gz \
        -2 $outdir/trim/${sampleid}.R2_val_2.fq.gz \
        -p $SLURM_CPUS_PER_TASK \
        | awk -v OFS='\t' '$0~/^@/ { print; next } { split($1,bc,":"); total=length(bc); print $0 "\tCB:Z:" bc[1] "\tUB:Z:" bc[2]}' \
        | $samtools sort -@ $thread_sort -m "12G" \
        | $samtools view -q 30 -b \
        > $outdir/align/${sampleid}.sorted.bam

    $samtools index $outdir/align/${sampleid}.sorted.bam
    $samtools stats $outdir/align/${sampleid}.sorted.bam > $outdir/align/${sampleid}.bam.stat

}


function align(){

    if [[ ! -d $outdir/align ]];then 
        mkdir -p $outdir/align
    fi 

    if [[ $paired == "TRUE" ]];then
        $bowtie2 -x $bowti2_idx \
            --no-unal \
            -X 2000 \
            -1 $outdir/trim/${sampleid}_clean_barcoded.R1.fastq.gz \
            -2 $outdir/trim/${sampleid}_clean_barcoded.R2.fastq.gz \
            -p $SLURM_CPUS_PER_TASK \
            | awk -v OFS='\t' '$0~/^@/ { print; next } { split($1,bc,":"); total=length(bc); print $0 "\tCB:Z:" bc[1] "\tUB:Z:" bc[2]}' \
            | $samtools sort -@ $thread_sort -m "12G" \
            | $samtools view -q 30 -b \
            > $outdir/align/${sampleid}.sorted.bam
    else
        $bowtie2 -x $bowti2_idx \
            -U $outdir/trim/${sampleid}_clean_barcoded.R1.fastq.gz \
            --no-unal \
            -p $SLURM_CPUS_PER_TASK \
            | awk -v OFS='\t' '$0~/^@/ { print; next } { split($1,bc,":"); total=length(bc); print $0 "\tCB:Z:" bc[1] "\tUB:Z:" bc[2]}' \
            | $samtools sort -@ $thread_sort -m "12G" \
            | $samtools view -q 20 -b \
            > $outdir/align/${sampleid}.sorted.bam
    fi 

    $samtools index $outdir/align/${sampleid}.sorted.bam
    $samtools stats $outdir/align/${sampleid}.sorted.bam > $outdir/align/${sampleid}.bam.stat

}


