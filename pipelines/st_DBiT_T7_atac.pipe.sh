function stat(){

    if [[ ! -d $outdir/stat ]];then 
        mkdir -p $outdir/stat 
    fi 
    
        
    bam="$outdir/bulk/${sampleid}.clean.bam"
    peaks="$outdir/bulk/${sampleid}_peaks.narrowPeak"

    # check file
    if [ ! -s $bam ]; then
        echo $bam " not exist"
        exit
    fi

    if [ ! -s $peaks ]; then
        echo $peaks "Read2 not exist"
        exit
    fi

    echo "→ Computing FRiP for $sampleid …"

    # 1) total reads
    total_reads=$(samtools view -c -@ 12 "$bam")

    # 2) reads in peaks
    reads_in_peaks=$( \
        $bedtools sort -i "$peaks" \
        | $bedtools merge -i stdin \
        | $bedtools intersect -u -nonamecheck \
            -a "$bam" \
            -b stdin -ubam \
        | samtools view -c \
    )

    # 3) 计算 FRiP（保留四位小数）
    frip=$(awk 'BEGIN{printf "%.4f", '$reads_in_peaks'/'$total_reads'}')

    # 写入结果
    if [[ ! -s ${outdir}/stat/stat.txt ]]; then
        echo -e "SampleID\tSpecies\t" \
            "Total_reads\tBarcode_valid_percent\t" \
            "Reads_high_qualtity_percent\t" \
            "Unique_aligned_reads\t" \
            "Unique_align_rate\t" \
            "median_dup\t" \
            "median_n_fragments\t" \
            "reads_in_peaks\t" \
            "frip" > ${outdir}/stat/stat.txt
    fi    
    valid_barcode_reads=$(less $outdir/QC/${sampleid}.mapped.tsv.gz | awk '{sum+=$2}END{print sum}')
    total_raw_reads=$(less $outdir/barcode/${sampleid}.total_readsN.txt  | xargs)
    valid_barcode_percent=$(echo "scale=4; $valid_barcode_reads / $total_raw_reads * 100" | bc | xargs)
    reads_passing_filter=$(grep 'output":' $outdir/trim/${sampleid}.trimming_report.txt | awk '{print $2}' | head -n 1 | sed 's/,//g')
    reads_passing_filter_rate=$(echo "scale=2; $reads_passing_filter / $valid_barcode_reads * 100" | bc | xargs)
    
    unique_align_reads=$($samtools view -q 20 -c -@ 12 $outdir/align/${sampleid}.sorted.bam | xargs)

    align_rate=$(echo "scale=2; $unique_align_reads / $reads_passing_filter * 100" | bc | xargs)

    total_line=$(less $outdir/fragments/${sampleid}_CB.metadata.txt | wc -l)
    median_n_line=$(echo "scale=0; $total_line / 2" | bc | xargs)
    median_n_frag=$(sort -k2,2n $outdir/fragments/${sampleid}_CB.metadata.txt | head -n $median_n_line | awk '{print $2}' | tail -n 1 | xargs)
    median_dup=$(sort -k3,3n $outdir/fragments/${sampleid}_CB.metadata.txt | head -n $median_n_line | tail -n 1 | awk '{printf "%.2f", $3 * 100}' | xargs)



    echo -e "$sampleid\t${species}\t" \
        "${total_raw_reads}\t" \
        "${valid_barcode_percent}%\t" \
        "${reads_passing_filter_rate}%\t" \
        "${unique_align_reads}\t" \
        "${align_rate}%\t" \
        "${median_dup}\t" \
        "${median_n_frag}\t" \
        "${reads_in_peaks}\t" \
        "${frip}" >> ${outdir}/stat/stat.txt
}


